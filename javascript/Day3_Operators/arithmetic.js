
// Operators

// Arithmetic operators

var a=10
var b=2

console.log(`The addition of ${a} and ${b} is : ${a+b}`);

console.log(`The subraction of ${a} and ${b} is : ${a-b}`);

console.log(`The multiplication of ${a} and ${b} is : ${a*b}`);

console.log(`The division of ${a} and ${b} is : ${a/0}`); // Infinity

console.log(`The division of ${a} and ${b} is : ${0/1}`);  // Zero

console.log(`The division of ${a} and ${b} is : ${-1/0}`); // -Infinity

console.log(`The modulus of ${a} and ${b} is : ${a%b}`);

console.log(`The power of ${a} and ${b} is : ${a**b}`);

